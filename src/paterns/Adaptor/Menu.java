public class Menu {
    int price;
    String sizeCola;
    String sizeBurger;
    boolean toy;


    public int getPrice() {
        return price;
    }

    public void setPrice(int price) {
        this.price = price;
    }

    public String getSizeCola() {
        return sizeCola;
    }

    public void setSizeCola(String sizeCola) {
        this.sizeCola = sizeCola;
    }

    public boolean getToy() {
        return toy;
    }

    public void setToy(boolean toy) {
        this.toy = toy;
    }

    public String getSizeBurger() {
        return sizeBurger;
    }

    public void setSizeBurger(String sizeBurger) {
        this.sizeBurger = sizeBurger;
    }

}

